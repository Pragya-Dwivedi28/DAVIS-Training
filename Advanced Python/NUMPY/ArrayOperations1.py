#perform mathematical functions on array

import numpy as np

arr = np.array([10,20,30,25,6,2])
print("The minimum of array is :",np.min(arr))
print("The maximum of array is :",np.max(arr))
print("The index of minimum of array is :",np.argmin(arr))
print("The index of maximum of array is :",np.argmax(arr))
print("The square root of array is :",np.sqrt(arr))
print("The sine value of array values are :",np.sin(arr))
print("The cosine value of array values are :",np.cos(arr))
